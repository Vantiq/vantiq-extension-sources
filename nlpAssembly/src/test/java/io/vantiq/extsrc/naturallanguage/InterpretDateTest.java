    private static String todayGMT() {
        // use a formatter
        DateTimeFormatter utcFormatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        
        Date date = new Date(); //; java.util.Date object
        Instant instant = date.toInstant();
        ZonedDateTime zonedDateTime = instant.atZone(ZoneOffset.UTC);
        String retVal = utcFormatter.format(zonedDateTime);
        log.debug("today is: {}", retVal); // 08/30/2021 03:58:05 GMT
        return retVal;
    }

